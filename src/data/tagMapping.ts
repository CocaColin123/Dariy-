// 工具函数
export function getDimension(name: string): TagDimension | undefined {
  return DIMENSIONS.find(d => d.name === name);
}

export function getSubTagCount(dimensionName: string, subTagName: string): number {
  const dim = getDimension(dimensionName);
  if (!dim) return 0;
  const sub = dim.subTags.find(s => s.name === subTagName);
  return sub?.count ?? 0;
}

export function getEntriesForSubTag(dimensionName: string, subTagName: string): string[] {
  const dim = getDimension(dimensionName);
  if (!dim) return [];
  const sub = dim.subTags.find(s => s.name === subTagName);
  return sub?.entries ?? [];
}
